int screenWidth = 320;
int screenHeight = 180;

// fake camera
typedef struct{
    float x;
    float y;
    int w;
    int h;
}View;

void InitGame(void);
void GameLoop(void);     // Update and Draw one frame
void (*Screen)(void);
void DefaultScreen(void);
void Inputs(void);

TileSet *tileSet;
